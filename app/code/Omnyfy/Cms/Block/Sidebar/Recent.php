namespace Omnyfy\Cms\Block\Sidebar;

/**
 * Cms sidebar categories block
 */
class Recent extends \Omnyfy\Cms\Block\Article\ArticleList\AbstractList
{
    use Widget;

    /**
     * @var string
     */
    protected $_widgetKey = 'recent_articles';

    /**
     * @return $this
     */
    public function _construct()
    {
        $this->setPageSize(
            (int) $this->_scopeConfig->getValue(
                'mfcms/sidebar/'.$this->_widgetKey.'/articles_per_page',
                \Magento\Store\Model\ScopeInterface::SCOPE_STORE
            )
        );
        return parent::_construct();
    }

    /**
     * Retrieve block identities
     * @return array
     */
    public function getIdentities()
    {
        return [\Magento\Cms\Model\Block::CACHE_TAG . '_cms_recent_articles_widget'  ];
    }

}
